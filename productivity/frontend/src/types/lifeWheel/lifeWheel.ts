/** Одна задача внутри направления */
export type WheelTask = {
    id: string;
    text: string;
    done: boolean;
};

/** Одно направление (сегмент круга) */
export type WheelSegment = {
    id: string;
    name: string;         // Название: "Здоровье", "Карьера" и т.д.
    score: number;        // Балл 0–10 — определяет радиус заливки
    color: string;        // Фиксированный цвет сегмента
    tasks: WheelTask[];
};