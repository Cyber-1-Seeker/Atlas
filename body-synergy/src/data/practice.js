// ── PROGRESS TRACKING ─────────────────────────────────────
export const SK_PRACTICE = 'bs_practice_v1'

export function loadPracticeProgress() {
    try {
        return JSON.parse(localStorage.getItem(SK_PRACTICE)) || {}
    } catch {
        return {}
    }
}

export function savePracticeProgress(p) {
    try {
        localStorage.setItem(SK_PRACTICE, JSON.stringify(p))
    } catch {
    }
}

export function toggleTechniqueLog(techniqueId) {
    const prog = loadPracticeProgress()
    const today = new Date().toISOString().slice(0, 10)
    const key = `${techniqueId}-${today}`
    prog[key] = !prog[key]
    savePracticeProgress(prog)
    return prog
}

export function getTechniqueTriedCount(techniqueId) {
    const prog = loadPracticeProgress()
    return Object.keys(prog).filter(k => k.startsWith(techniqueId + '-') && prog[k]).length
}