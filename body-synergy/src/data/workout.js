export function getTodaySchedule() {
    const today = new Date()
    const dayOfWeek = today.getDay() // 0=вс, 1=пн, ..., 6=сб

    if (dayOfWeek === 0) return {type: 'sunday'}

    // Пн=1 ... Сб=6 → индекс 0..4
    const workdayIndex = dayOfWeek - 1  // 0..5
    const totalDays = PROGRAM.totalDays
    const programDayIndex = workdayIndex % totalDays  // 0..totalDays-1
    const day = PROGRAM.days[programDayIndex]

    return {
        type: 'workout',
        day,
        weekdayIndex: workdayIndex,
        programDayNumber: programDayIndex + 1,
    }
}